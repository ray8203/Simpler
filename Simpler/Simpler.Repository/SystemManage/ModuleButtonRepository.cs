using Simpler.Data;
using Simpler.Domain.Entity.SystemManage;
using Simpler.Domain.IRepository.SystemManage;
using Simpler.Repository.SystemManage;
using System.Collections.Generic;

namespace Simpler.Repository.SystemManage
{
    public class ModuleButtonRepository : RepositoryBase<ModuleButtonEntity>, IModuleButtonRepository
    {
        public void SubmitCloneButton(List<ModuleButtonEntity> entitys)
        {
            using (var db = new RepositoryBase().BeginTrans())
            {
                foreach (var item in entitys)
                {
                    db.Insert(item);
                }
                db.Commit();
            }
        }
    }
}
